def scene_id(n: int) -> str:
    return f"SCENE_{n:03d}"

def all_scene_ids() -> List[str]:
    result: List[str] = []
    for start, end in ACT_RANGES.values():
        result.extend(scene_id(n) for n in range(start, end + 1))
    return result

def act_for(n: int) -> int:
    for act, (start, end) in ACT_RANGES.items():
        if start <= n <= end:
            return act
    raise ValueError(n)

def phase(act: int, n: int) -> str:
    start, end = ACT_RANGES[act]
    r = (n - start) / max(1, end - start)
    names = {1: ["Надежда", "Исследование", "Сомнение", "Первая ночь"], 2: ["Отсутствие", "Разлом", "Правда", "Вторая ночь"], 3: ["Последний шанс", "Ответы", "Кульминация", "Последствия"]}[act]
    return names[min(3, int(r * 4))]

def location(act: int, n: int) -> str:
    locs = {1: ["Двор", "Административный корпус", "Генераторная", "Столовая"], 2: ["Столовая", "Коридоры", "Архивный блок", "Подземный уровень"], 3: ["Центральный контур", "Архив", "Лаборатория", "Финальный шлюз"]}[act]
    return locs[n % len(locs)]

