def generate_scenes() -> List[Dict[str, Any]]:
    scenes = []
    for sid in all_scene_ids():
        n = int(sid.split("_")[1])
        act = act_for(n)
        key = KEY_SCENES.get(sid, {})
        scene = {
            "id": sid,
            "act": act,
            "act_name": ACT_NAMES[act],
            "title": key.get("title") or f"{phase(act, n)} · узел {sid}",
            "time": key.get("time") or f"День {act}",
            "location": key.get("location") or location(act, n),
            "text": key.get("text") or default_text(act, n),
            "image": IMAGE_MAP.get(sid),
            "choices": key.get("custom_choices") or default_choices(act, n),
        }
        if sid in {"SCENE_061", "SCENE_276", "SCENE_461"}:
            scene["effects_on_enter"] = {"docs": ["doc_px17_intro"]}
        if sid == "SCENE_016":
            scene["effects_on_enter"] = {"docs": ["doc_fedor_file"]}
        if sid == "SCENE_501":
            scene["effects_on_enter"] = {"docs": ["doc_zorin_diary"]}
        scenes.append(scene)
    return scenes

def get_scenes() -> Dict[str, Dict[str, Any]]:
    return {s["id"]: s for s in generate_scenes()}

def get_documents() -> Dict[str, Dict[str, str]]:
    return DOCUMENTS
