function render(app) {
  const panel = document.getElementById("appPanel");
  const data = DATA[app];
  if (!data) return;
  panel.innerHTML = `
    <h2 class="${data.glitch ? "glitch" : ""}">${data.title}</h2>
    <p>${data.body}</p>
    <div class="list">
      ${data.items.map(item => `<div class="item">${item}</div>`).join("")}
    </div>
  `;
}

document.querySelectorAll("button[data-app]").forEach(btn => {
  btn.addEventListener("click", () => render(btn.dataset.app));
});

const params = new URLSearchParams(window.location.search);
const app = params.get("app");
if (app) render(app);
